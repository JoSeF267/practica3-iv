					<h1><a href="#" target="_self">'La Stampa' dice que su corresponsal en Siria lleva 20 días desaparecido</a></h1>
					
					

					<p>EEl corresponsal en Siria del diario italiano 'La Stampa', Domenico Quirico, lleva 20 días desaparecido, desde que entró en el país a través de la frontera con Líbano, según ha informado este lunes el editor de 'La Stampa', Mario Calabresi. Quirico salió de Italia el 5 de abril rumbo a Beirut, donde esperaba &quot;materializar&quot; sus contactos antes de cruzar la frontera hacia Siria. Un día después, ya había entrado en territorio sirio para dirigirse a Homs y, si era posible, hacia Damasco.</p>
					<p>El 8 de abril habló por teléfono con su mujer para informarla de que estaba bien y explicarla que a partir de ese momento no utilizaría el teléfono porque la gente que viajaba con él le había pedido que no usara comunicación satelital. Al día siguiente envió un mensaje de texto a un periodista de la televisión pública italiana, RAI, informándole de que estaba de camino a Homs. Desde entonces, no se sabe nada del corresponsal de 'La Stampa'.</p>
					<p>A pesar de su silencio, el diario italiano no se preocupó porque &quot;forma parte de su manera de trabajar&quot;. &quot;Siempre ha dicho que las tecnologías y la comunicación son la mejor forma de llamar la atención y de ponerse en peligro&quot;, ha indicado Calabresi. No obstante, el 15 de abril la familia de Quirico decidió informar de la situación a la Unidad de Crisis del Ministerio de Relaciones Exteriores. Entonces comenzó la búsqueda, que se llevó en secreto para no llamar la atención sobre su presencia en Siria, ante la posibilidad de que estuviera intentando abandonar el país.</p>
					<p>&quot;Hasta ahora, la gran angustia de su familia y de todos nosotros se ha mantenido en secreto, e incluso colegas y amigos de otros diarios han respetado este silencio, que esperábamos nos llevara a una solución. Pero por desgracia no ha sido así y por eso hemos decidido hacer pública su desaparición&quot;, ha explicado. Calabresi ha destacado que Quirico, de 62 años de edad, &quot;es uno de los periodistas italianos más preparados para hacer frente a situaciones de peligro&quot;. Era la cuarta vez que estaba en Siria para contar la guerra civil. Las dos primeras estuvo en Aleppo y la tercera en Idleb.</p>
					Otros conflictos
					<p>Además, ha cubierto el conflicto en Malí, el conflicto en Darfur y la llamada Primavera Arabe --Túnez, Egipto y Libia, donde fue secuestrado junto a otros dos periodistas italianos-- y ha estado en Somalia y Uganda. &quot;Dominic nunca ha accedido a contar las cosas desde el otro lado de la frontera, a través de las voces de los refugiados o exiliados, porque considera que es éticamente inaceptable. Siempre dice que hay que estar dentro de los hechos, que para contar un bombardeo hay que estar bajo las bombas&quot;, ha dicho.</p>
					<p>&quot;Seguimos estando tenazmente apegados a la esperanza de tener pronto noticias suyas, para seguir escuchando sus historias, y su capacidad de análisis no ideológico o partidista. Esperamos junto a su esposa, hijas, amigos y nuestros lectores&quot;, ha concluido. A principios del mes de abril, cuatro periodistas italianos fueron secuestrados en Siria y liberados apenas una semana después. La guerra civil ha convertido a Siria en uno de los países más peligrosos de la región, también para la prensa internacional.</p>
