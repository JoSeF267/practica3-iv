					<h2><a href="#" target="_self">Los robos se convierten en el delito que más crece en el primer trimestre, aunque con cifras similares a 2011</a><a href=""></a></h2>
					

					<p>Para sorpresa de propios y extraños, el primer trimestre del año 2012 arrojó unos datos sobre criminalidad en Granada poco habituales. En plena crisis económica, el porcentaje de robos con fuerza descendió un 18% respecto al mismo periodo del año 2011: traducido en cifras, tanto la Policía Nacional como la Guardia Civil registraron 1.560 robos con fuerza frente a los 1.911 del trimestre de 2011. Ahora, en los tres primeros meses de 2013, los números vuelven a su acomodo habitual y aquel descenso en robos con fuerza se convierte en un ascenso ahora, comparando el primer trimestre de 2012 con el de 2013.Los últimos datos del Ministerio del Interior cuantifican en 1.819 las denuncias recibidas por robos con fuerza, un 16% más, aunque un 2% por debajo del mismo periodo de 2011. Sube, pero dentro de unos márgenes soportables en cuanto a números absolutos.</p>
					<p>El artículo 240 del Código Penal castiga con penas de uno a tres años de cárcel los robos con fuerza, aunque si se cometen en viviendas o locales públicos la condena sube de tres a cinco años, según el artículo 241 de del Código Penal.</p>
					<p>Uno de los apartados incluidos en la sección de robos con fuerza son los asaltos a viviendas que no dejan de crecer en esta provincia desde el segundo trimestre de 2012 con una media de cinco al día. La comparación del primer trimestre de 2012 con los tres primeros meses de 2013 arroja un incremento del 21% en el número de viviendas asaltadas con cien robos más en pisos respecto al mismo periodo del año anterior.</p>
					<p>También aumentan muy ligeramente los robos con violencia e intimidación, aquellos donde suele haber agresión física o amenazas con armas de fuego o de las consideradas blancas.Crecen en diez robos más de este tipo. También suben un 3%.</p>
					<p>Pese a todo, en el cómputo general de delitos y faltas se registra un descenso de 0,9 puntos respecto al mismo periodo del ejercicio 2012, teniendo en cuenta que ha estadíos como el de homicidios donde el descenso ha sido de un 66% con solo dos casos frente a los seis del primer trimestre de 2012. Los indicadores de seguridad que se especifican en este informe comprenden diferentes categorías: cifra total de delitos y faltas, homicidios dolosos y asesinatos consumados, delincuencia violenta, robo con violencia o intimidación, robos con fuerza, sustracción de vehículos a motor, tráfico de drogas, daños, y hurtos. La casi totalidad de estos indicadores son los que establece y determina la Oficina Europea de Estadística (Eurostat).</p>
					<p>Entre los indicadores que presentan valores a la baja enGranada, cabe subrayar el descenso de los homicidios dolosos y asesinatos consumados (-66,7%) y los daños (-14,9%). Asimismo, han experimentado esta tendencia negativa, de manera importante, los delitos de sustracción de vehículos a motor, con un -18,5% menos.</p>
					<p>A lo largo del primer trimestre de 2013 se han conocido un total de 9.829 delitos y faltas en Granada, lo que supone un 0,9% menos respecto a los tres primeros meses del año pasado. Uno de cada tres infracciones de las registradas se corresponde a un hurto.Solo en estos noventa primeros días del año se denunciaron en Granada y su provincia 3.366 hurtos.</p>
