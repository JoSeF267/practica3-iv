					<h1><a href="#" target="_self">El Madrid muere a un gol de la gesta y cierra el proyecto</a></h1>
					
					

					<p>Las acusaciones reclamaban una sentencia ejemplar para limpiar la imagen del deporte español y que se entregasen las bolsas de sangre de la &lsquo;operación Puerto&rsquo; para poder identificar a los clientes de Eufemiano Fuentes, pero no hubo sorpresas. Casi siete años después de que estallase el escándalo, la mayor trama de dopaje descubierta en España se saldó este martes con una condena leve al doctor canario y al preparador físico Ignacio Labarta por un delito contra la salud pública.</p>
					<p>Un año de prisión y cuatro de inhabilitación para ejercer como médico deportivo a Eufemiano y cuatro meses de prisión a su «cómplice» Labarta. No ingresarán en la cárcel al no ser las penas superiores a dos años y por carecer de antecedentes. Los otros tres imputados (los ex directores deportivos Manolo Sainz y Vicente Belda; y Yolanda Fuentes, hermana de Eufemiano y también médico) han quedado absueltos.</p>
					<p>La juez Julia Patricia Santamaría ha rechazado facilitar a las autoridades deportivas y antidopaje las bolsas de sangre que le fueron incautadas a Eufemiano en mayo de 2006 y ha ordenado destruirlas, cuando la sentencia sea firme. Así, después de que el cabecilla de la &lsquo;operación Puerto&rsquo; revelase ante la magistrada que no solo trató a ciclistas, sino también a futbolistas, tenistas, atletas y boxeadores, no se conocerán más nombres de deportistas que pasaron por la consulta de Eufemiano Fuentes. La sentencia será recurrida ante la Audiencia Provincial de Madrid y podría llegar posteriormente hasta el Tribunal Supremo, por lo que la resolución definitiva se demoraría al menos cuatro años más.</p>
					<p>El veredicto del Juzgado de lo Penal, contrario a los intereses de la Agencia Mundial Antidopaje (AMA), entre otros organismos internacionales, supone un freno importante a las aspiraciones olímpicas de Madrid 2020. No se juzgaba un delito de dopaje, porque los hechos son anteriores a la ley que entró en vigor en febrero de 2007. Cuando se descubrió la red encabezada por Eufemiano no estaba aún tipificado como tal el suministro de drogas en el deporte. Sin embargo, a Eufemiano ni siquiera se le ha impuesto el castigo máximo como reconocido «autor de un delito contra la salud pública» (dos años de prisión), aunque la Abogacía del Estado solicitaba dos años y medio por el agravante de precio.</p>
					<p>Más difícil de entender aún, tanto en España como en el extranjero, es que la magistrada haya decidido anular una prueba considerada clave en el proceso: la sangre. La entrega de las más de 200 bolsas intervenidas por la Guardia Civil, con códigos, cifras y apodos, hubiera permitido que se cotejase el ADN de los deportistas que se sometieron a los tratamientos de Eufemiano para mejorar su rendimiento y no solo conocer a más tramposos, sino abrir también la posibilidad de imponerles castigos deportivos. El único ciclista español identificado y sancionado por su implicación en la &lsquo;operación Puerto&rsquo; ha sido Alejandro Valverde.</p>
					<p>Medio mundo ya quedó frustrado durante el juicio cuando el doctor canario se ofreció a facilitar públicamente los nombres de sus pacientes y la magistrada rechazó el reto. Este martes, en una sentencia de 384 folios, se dio un paso más al acordar «el comiso y la destrucción de las bolsas de sangre, plasma y concentrados de hematíes y de su contenido aprehendidas en los domicilios de Eufemiano Fuentes y José Luis Merino Batres (hematólogo liberado por padecer Alzheimer)» y decretar que «no ha lugar a la entrega de muestras solicitada por la Federación Española de Ciclismo (FEC), AMA, Unión Ciclista Internacional (UCI) y Comité Olímpico Italiano (CONI)».</p>
					<p>Aunque se considera probado queEufemiano realizó transfusiones sanguíneas y que también dopó a sus clientes con EPO, hormonas de crecimiento, testosterona, insulina y hormonas femeninas, entre otros productos prohibidos, y que ello supuso «un importante peligro para la salud del ciclista», el principal imputado en la &lsquo;operación Puerto&rsquo; puede estar muy satisfecho con la sentencia. También se le ha multado con 4.500 euros, a razón de una cuota diaria de 15 euros durante diez meses, pero al ginecólogo ni siquiera se le ha inhabilitado para desarrollar su profesión como médico de familia. Tan solo, «para el ejercicio de la medicina deportiva».</p>
